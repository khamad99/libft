#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int		len;
	unsigned int		i;
	char				*r;

	if (!s || !f)
		return (0);
	len = ft_strlen(s);
	r = (char *) malloc(sizeof(char) * (len + 1));
	if (!r)
		return (0);
	i = 0;
	while (s[i])
	{
		r[i] = f(i, s[i]);
		i++;
	}
	r[i] = '\0';
	return (r);
}
